package com.estudosJava.AprogramacaoOrientadaAObjetoIntroducao.dominio;

public class Carro {
     public String nome;
     public String modelo;
     public int ano;
     public double kmRodado;

     public Carro(String nome, String modelo, int ano, double kmRodado) {
          this.nome = nome;
          this.modelo = modelo;
          this.ano = ano;
          this.kmRodado = kmRodado;
     }
}
